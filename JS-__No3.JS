// 1. Написати програму яка перевіряє чи введене число через prompt потрапляє в діапазон 
// від 55 до 99 включно. Якщо так, вивести в консоль "Число потрапляє в діапазон". 
// Якщо ні, вивести в консоль "Число не потрапляє в діапазон".
if (99 > prompt('Введіть дані:') && prompt('Введіть дані:') > 55) {
    console.log('Число потрапляє в діапазон');
  } else {
    console.log('Число не потрапляє в діапазон');
  }

// 2. Є 3 вікові групи : діти (0-16), дорослі(17-60), пенсіонери (61-100). 
// До якої групи відноситься Максим, якщо йому 55 років. Назву групи вивести на екран.
const age = 55;
if (16 > age && age > 0) {
    console.log('Діти');
  } else if (60 > age && age > 17) {
    console.log('Дорослі');
  } else if (61 > age && age > 100) {
    console.log('Пенсіонери');
  } else {
    console.log('Ви занадто старий');
  }

